package TicTacQuest;

public class Entity {
    String name;
    double health;
    double attackDmg;
    double luck;

    public void setName(String name) {
        this.name = name;
    }

    public void setHealth(double health) {
        this.health = health;
    }

    public void setAtkDmg(double atk) {
        this.attackDmg = atk;
    }

    public void setLuck(double luck) {
        this.luck = luck;
    }
}
